// 4. prefix - suffix

// 사전적 의미
// prefix (접두사) : 특정 단어의 앞에 붙는 것
// suffix (접미사) : 특정 단어의 뒤에 붙는 것

// prefix의 사례
// 1. React Hook앞에 use를 붙히는 것
// 2. jQuery앞에 $를 붙히는 것
// 3. JavaScript에서 #을 앞에 붙혀 private으로 쓰는 것

// suffix의 사례
// 1. 네트워크 요청을 할때 뒤에 request, success, failure를 사용
// 2. 복수 단위의 파일 뒤에 s를 붙힘

// 코드의 일관성을 맞추기에 suffix, prefix의 사용이 매우 유용함
